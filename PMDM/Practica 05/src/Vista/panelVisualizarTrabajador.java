    public void iniciarDatos() throws SQLException {
        
        t = gestor.inicialDetalle("select * from trabajador, empresa where empresa.nomEmpresa = '" + nombre + "' and empresa.codigoEmpresa=Trabajador.codEmpresa");
        mostrarDatos(t);
    }
    
    //muestro los datos iniciados 
    
    public void mostrarDatos(Trabajador t) throws SQLException{
        codTText.setText("" + t.getCodTrabajador());
        nomText.setText(t.getNomTrabajador());
        apeText.setText(t.getApeTrabajador());
        salText.setText("" + t.getSalario());
        fecText.setText("" + t.getFechaAlta());
        
        ImageIcon icon  = new ImageIcon("./src/Fotos/" + t.getFotoTrabajador());
        
        imagen.setIcon(icon);
        
        
        
        //Valido los extremos de la vista 
        
        if(gestor.comprobarAnt())
            antButton.setEnabled(true);
        else
            antButton.setEnabled(false);
        if(gestor.comprobarSig())
            sigButton.setEnabled(true);
        else
            sigButton.setEnabled(false);
    }
    
    //actualizo los cambios en la vista
    
    void actualizar(int codigo) throws SQLException {
        Trabajador t;
        t = gestor.inicialDetalle("select * from trabajador, empresa where empresa.nomEmpresa = '" + nombre + "' and empresa.codigoEmpresa=trabajador.codEmpresa");
        while(t.getCodTrabajador()!=codigo){
            t=gestor.sig();
        }
        mostrarDatos(t);
    }
