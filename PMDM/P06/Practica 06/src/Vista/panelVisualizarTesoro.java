    public void inicializar(String usuario) {
        mostrarDatos(BD.Inicializar("select tesoro.* from tesoro, banda where banda.codBanda = tesoro.bandaPertenece AND banda.nomBanda = '" + usuario + "'"));
    }
    
    private void mostrarDatos(Tesoro t) {
        
        codText.setText("" + t.getCodTesoro());
        nomText.setText("" + t.getNomTesoro());
        
        if(t.getBandaPertenece() == 1){
            bandText.setText("Mugiwara");
        }else if(t.getBandaPertenece() == 2){
            bandText.setText("RedHair");
        }else if(t.getBandaPertenece() == 3){
            bandText.setText("Heart");
        }else if(t.getBandaPertenece() == 4){
            bandText.setText("Buggy");
        }
        
        
        try {
            if (BD.hasNext()) {
                sig.setEnabled(true);
                last.setEnabled(true);
            } else {
                sig.setEnabled(false);
                last.setEnabled(false);
            }
            if (BD.hasPrev()) {
                ant.setEnabled(true);
                prin.setEnabled(true);
            } else {
                ant.setEnabled(false);
                prin.setEnabled(false);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ControlErrores.catchError(4));
        }
    }
    
