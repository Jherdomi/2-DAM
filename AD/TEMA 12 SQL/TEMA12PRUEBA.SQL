DECLARE
	cursor C1 IS SELECT DNOMBRE FROM DEPART;
	V1 DEPART.DNOMBRE%TYPE;
BEGIN
	OPEN C1;
	
	FETCH C1 INTO V1;
	IF C1%NOTFOUND THEN
		DBMS_OUTPUT.PUT_LINE('No hay datos.');
	ELSE
		WHILE C1%FOUND LOOP
	
			DBMS_OUTPUT.PUT_LINE(V1);
			FETCH C1 INTO V1;
		END LOOP;
	END IF;
	
	CLOSE C1;
	
END;

